#!/usr/bin/python3
def add(a, b):
    """My addition function.
    Args:
        a: first intiger
        b: second intiger
    Returns:
          The return value. a + b
    """
    return (a + b)

def sup(a, b):
    """My subtraction function.
    Args:
        a: first intiger
        b: second intiger
    Returns:
          The return value. a - b
    """
    return (a - b)

def mul(a, b):
    """My multiplication function.
    Args:      
       a: first intiger
       b: second intiger
    Return:
        The return value. a * b 
    """
    return (a * b)

def div(a, b):
    """My division function.
    Args:
        a: first intiger
        b: second intiger
    Return:
        The return value. a / b
    """
    return int(a / b)

